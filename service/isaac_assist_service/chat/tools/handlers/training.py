@with_telemetry
async def _handle_get_training_status(args: Dict) -> Dict:
    """Read TensorBoard event files and subprocess state for an RL training run.

    Scans the log directory for ``events.out.tfevents.*`` files, attempts to
    parse the most recent one with ``tensorboard.EventAccumulator`` to extract
    the latest reward scalar and global step, and checks a ``launcher.pid``
    file to determine whether the training process is still alive.  Degrades
    gracefully when TensorBoard is not installed.

    Args:
        args: tool-call args dict containing:
            - run_id (str): training run identifier; used to build the default
              log directory ``workspace/rl_checkpoints/<run_id>``.
            - log_dir (str, optional): explicit log directory path override.

    Returns:
        Dict[str, Any] with keys:
            - run_id (str): as supplied.
            - log_dir (str): resolved log directory.
            - state (str): ``"running"``, ``"finished"``, ``"starting"``,
              ``"missing"``, or ``"unknown"``.
            - step (int or None): last recorded global step.
            - total_steps (int or None): planned total steps (None if unknown).
            - latest_reward (float or None): latest reward scalar.
            - events_found (int): number of TensorBoard event files found.
            - pid (int): launcher PID when available.
            - note/error (str): informational messages on degraded paths.
    """
    import os
    from pathlib import Path
    # Phase 8 wave 24 — _WORKSPACE migrated.

    run_id = args["run_id"]
    log_dir = args.get("log_dir") or str(_WORKSPACE / "rl_checkpoints" / run_id)

    log_path = Path(log_dir)
    result: Dict[str, Any] = {
        "run_id": run_id,
        "log_dir": str(log_path),
        "state": "unknown",
        "step": None,
        "total_steps": None,
        "latest_reward": None,
        "events_found": 0,
    }

    if not log_path.exists():
        result["state"] = "missing"
        result["error"] = f"log dir does not exist: {log_path}"
        return result

    # Look for TensorBoard event files (events.out.tfevents.*)
    event_files = sorted(log_path.glob("**/events.out.tfevents.*"))
    result["events_found"] = len(event_files)

    if not event_files:
        result["state"] = "starting"
        return result

    # Try to parse the latest event file. tensorboard isn't a hard dep, so we
    # fall back gracefully on import failure.
    latest = event_files[-1]
    try:
        from tensorboard.backend.event_processing.event_accumulator import EventAccumulator  # type: ignore
        acc = EventAccumulator(str(latest), size_guidance={"scalars": 0})
        acc.Reload()
        scalars = acc.Tags().get("scalars", [])
        # Prefer common reward / step tag names
        for tag in ("reward", "Train/reward", "train/reward",
                    "rollout/ep_rew_mean", "Episode_Reward/Mean"):
            if tag in scalars:
                events = acc.Scalars(tag)
                if events:
                    result["latest_reward"] = events[-1].value
                    result["step"] = events[-1].step
                    break
        if result["step"] is None and scalars:
            events = acc.Scalars(scalars[0])
            if events:
                result["step"] = events[-1].step
    except ImportError:
        result["note"] = "tensorboard not installed — install with `pip install tensorboard`"
    except Exception as exc:
        result["error"] = f"failed to parse event file: {exc}"

    # Subprocess state via the launcher's pid file (if launch_training wrote one)
    pid_file = log_path / "launcher.pid"
    if pid_file.exists():
        try:
            pid = int(pid_file.read_text().strip())
            # Cheap liveness check: send signal 0
            try:
                os.kill(pid, 0)
                result["state"] = "running"
                result["pid"] = pid
            except ProcessLookupError:
                result["state"] = "finished"
                result["pid"] = pid
            except PermissionError:
                # Process exists but owned by another user — still treat as running
                result["state"] = "running"
                result["pid"] = pid
        except Exception as exc:
            result["state"] = "unknown"
            result["error"] = f"could not read launcher.pid: {exc}"
    elif result["events_found"] > 0:
        result["state"] = "running"

    return result


