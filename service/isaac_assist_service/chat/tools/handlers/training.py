def _build_mutation_prompt(prev_reward: str, metrics: Dict, user_feedback: Optional[str]) -> str:
    """Compose a mutation prompt for the next Eureka reward iteration.

    Embeds the previous reward source, per-component training metrics
    (via ``_format_component_metrics``), overall task success rate, and
    optional user feedback into a single LLM prompt string.

    Args:
        prev_reward (str): Python source of the reward function from the
            previous Eureka iteration.
        metrics (dict): metrics blob from ``evaluate_reward``; expected keys
            are ``components`` (dict), ``task_success_rate`` (float), and
            ``fitness`` (float).
        user_feedback (str or None): free-form guidance from the user; if
            ``None`` the feedback section is omitted.

    Returns:
        str: formatted mutation prompt ready to send to the LLM.
    """
    prompt = f"""Previous reward function:
{prev_reward}

Training metrics per component:
{_format_component_metrics(metrics)}

Task success rate: {metrics.get('task_success_rate', 'N/A')}
"""
    if user_feedback:
        prompt += f"\nUser feedback: {user_feedback}\n"
    prompt += "\nBased on this data, generate an improved reward function."
    return prompt

def _read_tb_scalars(run_dir: str, tag: str) -> List[float]:
    """Read a TensorBoard scalar tag from event files in run_dir.

    Returns a chronologically ordered list of values. Returns [] if no event
    files are found, the tag is missing, or TensorBoard is not installed
    (we fall back gracefully so diagnostics still run on partial data).
    """
    try:
        from tensorboard.backend.event_processing.event_accumulator import (
            EventAccumulator,
        )
    except ImportError:
        logger.warning("[RLDebug] tensorboard not installed — TB scalar reads disabled")
        return []

    run_path = Path(run_dir)
    if not run_path.exists():
        return []

    # EventAccumulator handles both files and directories; pass the dir.
    try:
        acc = EventAccumulator(
            str(run_path),
            size_guidance={"scalars": 0},  # 0 == load all
        )
        acc.Reload()
        if tag not in acc.Tags().get("scalars", []):
            return []
        return [float(e.value) for e in acc.Scalars(tag)]
    except Exception as e:
        logger.warning(f"[RLDebug] TB read failed for {tag}: {e}")
        return []

def _read_checkpoint_action_std(run_dir: str) -> Optional[float]:
    """Read mean policy action std from the latest .pt checkpoint, if any.

    Scans ``run_dir`` recursively for ``.pt`` files, loads the most recent
    one via ``torch.load`` (weights_only=False to handle RSL-RL pickled
    configs), and extracts the mean standard deviation of the stochastic
    policy head.  Supports RSL-RL checkpoint keys ``std``, ``log_std``, and
    ``action_std`` under ``model_state_dict``.

    Args:
        run_dir (str): path to an IsaacLab / RSL-RL training run directory.

    Returns:
        float: mean action std across all action dimensions, or ``None``
        when no checkpoint is found, torch is not installed, or the std key
        is absent.
    """
    run_path = Path(run_dir)
    if not run_path.exists():
        return None
    ckpts = sorted(run_path.glob("**/*.pt"))
    if not ckpts:
        return None
    try:
        import torch  # type: ignore
        # weights_only=False because RSL-RL checkpoints contain pickled cfgs.
        state = torch.load(str(ckpts[-1]), map_location="cpu", weights_only=False)
        # RSL-RL stores 'model_state_dict'; key is typically 'std' or 'log_std'.
        sd = state.get("model_state_dict", state)
        for key in ("std", "log_std", "action_std"):
            if key in sd:
                t = sd[key]
                if key == "log_std":
                    t = t.exp()
                return float(t.mean().item())
        return None
    except Exception as e:
        logger.warning(f"[RLDebug] checkpoint std read failed: {e}")
        return None

def _generate_actuator_net_script(
    real_data_path: str,
    articulation_path: str,
    hidden_dim: int,
    num_layers: int,
    num_epochs: int,
    output_dir: str,
) -> str:
    """Generate an IsaacLab ActuatorNetLSTM training script.

    Produces a self-contained Python source string that trains a small LSTM
    to predict commanded joint torques from position error and joint velocity,
    using real hardware trajectory data stored in an HDF5 file.  The network
    architecture matches the IsaacLab ``ActuatorNetLSTM`` interface.

    The generated script:
    1. Loads ``(q_target, q, qd, tau)`` from the HDF5 file at
       ``REAL_DATA_PATH``.
    2. Constructs input features as ``[q_target - q, qd]`` per joint.
    3. Trains with Adam + MSE loss for ``NUM_EPOCHS`` epochs.
    4. Saves the model state dict and config to ``OUTPUT_DIR/actuator_net.pt``
       and writes a ``result.json`` with the final loss.

    Args:
        real_data_path (str): path to the HDF5 file containing the trajectory
            data (keys: ``joint_positions``, ``joint_velocities``,
            ``joint_torques_commanded``).
        articulation_path (str): USD prim path of the robot articulation (used
            only as a label in the script header).
        hidden_dim (int): LSTM hidden state dimensionality.
        num_layers (int): number of LSTM layers.
        num_epochs (int): training epochs.
        output_dir (str): directory where ``actuator_net.pt`` and
            ``result.json`` are written.

    Returns:
        str: complete Python source for the training script.
    """
    return f'''"""Auto-generated ActuatorNet (LSTM) training script.
Articulation: {articulation_path}
Real data:    {real_data_path}
"""
from __future__ import annotations
import json
from pathlib import Path

import h5py
import numpy as np
import torch
import torch.nn as nn
from torch.utils.data import DataLoader, TensorDataset

REAL_DATA_PATH = {real_data_path!r}
ARTICULATION_PATH = {articulation_path!r}
OUTPUT_DIR = Path({output_dir!r})
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

HIDDEN_DIM = {hidden_dim}
NUM_LAYERS = {num_layers}
NUM_EPOCHS = {num_epochs}


class ActuatorLSTM(nn.Module):
    def __init__(self, input_dim, hidden_dim, num_layers, output_dim):
        super().__init__()
        self.lstm = nn.LSTM(input_dim, hidden_dim, num_layers, batch_first=True)
        self.head = nn.Linear(hidden_dim, output_dim)

    def forward(self, x):
        out, _ = self.lstm(x)
        return self.head(out)


def load_pairs(path):
    with h5py.File(path, "r") as f:
        q_target = f["joint_positions_target"][:] if "joint_positions_target" in f else f["joint_positions"][:]
        q = f["joint_positions"][:]
        qd = f["joint_velocities"][:]
        tau = f["joint_torques_commanded"][:]
    x = np.stack([q_target - q, qd], axis=-1)  # (T, n_joints, 2)
    y = tau
    return x, y


def main():
    x, y = load_pairs(REAL_DATA_PATH)
    n_joints = x.shape[1]
    x_t = torch.tensor(x, dtype=torch.float32).reshape(1, x.shape[0], n_joints * 2)
    y_t = torch.tensor(y, dtype=torch.float32).reshape(1, y.shape[0], n_joints)
    ds = TensorDataset(x_t, y_t)
    dl = DataLoader(ds, batch_size=1)
    model = ActuatorLSTM(n_joints * 2, HIDDEN_DIM, NUM_LAYERS, n_joints)
    opt = torch.optim.Adam(model.parameters(), lr=1e-3)
    loss_fn = nn.MSELoss()
    losses = []
    for epoch in range(NUM_EPOCHS):
        for xb, yb in dl:
            pred = model(xb)
            loss = loss_fn(pred, yb)
            opt.zero_grad()
            loss.backward()
            opt.step()
        losses.append(float(loss.item()))
        if epoch % 20 == 0:
            print(f"epoch {{epoch}} loss={{loss.item():.6f}}")
    ckpt = OUTPUT_DIR / "actuator_net.pt"
    torch.save({{"model": model.state_dict(), "config": {{
        "hidden_dim": HIDDEN_DIM,
        "num_layers": NUM_LAYERS,
        "input_dim": n_joints * 2,
        "output_dim": n_joints,
    }}}}, ckpt)
    (OUTPUT_DIR / "result.json").write_text(json.dumps({{
        "checkpoint": str(ckpt),
        "final_loss": losses[-1] if losses else None,
        "num_epochs": NUM_EPOCHS,
    }}, indent=2))
    print(f"ActuatorNet saved to {{ckpt}}")


if __name__ == "__main__":
    main()
'''

