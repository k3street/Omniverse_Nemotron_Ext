@with_telemetry
async def _handle_suggest_dr_ranges(args: Dict) -> Dict:
    """Suggest domain randomization parameter ranges for a given task and robot.

    Looks up a base range set from ``_DR_TASK_DEFAULTS`` (matched by
    substring) then applies robot-specific overrides from ``_DR_ROBOT_HINTS``
    (e.g. Franka/Panda gripper friction, UR10 damping).  Optionally reads a
    real sensor CSV or JSON file to replace heuristic ranges with empirical
    min/max values derived from actual hardware data.

    Args:
        args: tool-call args dict containing:
            - task_type (str): task description, e.g. ``"pick_and_place"``,
              ``"locomotion"``, ``"navigation"``, ``"assembly"``.
            - robot (str, optional): robot name for hint matching (e.g.
              ``"franka"``, ``"ur10"``, ``"anymal"``).
            - real_data_path (str, optional): path to a CSV or JSON file
              with real sensor data; numeric columns become empirical range
              overrides.

    Returns:
        Dict[str, Any] with keys:
            - task_type (str): as supplied.
            - task_matched (str): matched ``_DR_TASK_DEFAULTS`` key.
            - robot (str): as supplied.
            - robot_matched (str or None): matched ``_DR_ROBOT_HINTS`` key.
            - suggested_ranges (dict[str, list[float]]): parameter → [min, max].
            - real_data_used (bool): whether empirical refinement was applied.
            - notes (list[str]): warnings about missing files or parse errors.
            - message (str): human-readable summary.
            - error (str): present when task_type is empty.
    """
    import csv
    import json
    from pathlib import Path
    from typing import List as _List
    # Phase 8 wave 15 — _DR_TASK_DEFAULTS migrated.
    task_raw = (args.get("task_type") or "").strip()
    robot_raw = (args.get("robot") or "").strip()
    real_data_path = args.get("real_data_path")

    if not task_raw:
        return {"error": "task_type is required"}

    task_lc = task_raw.lower().replace("-", "_").replace(" ", "_")
    robot_lc = robot_raw.lower()

    # Pick the closest matching task block.
    task_key = None
    for k in _DR_TASK_DEFAULTS:
        if k in task_lc or task_lc in k:
            task_key = k
            break
    if task_key is None:
        # Fall back to manipulation-style defaults.
        task_key = "pick_and_place"

    suggested = {k: list(v) for k, v in _DR_TASK_DEFAULTS[task_key].items()}

    # Robot-specific overrides.
    robot_match = None
    for k, v in _DR_ROBOT_HINTS.items():
        if k in robot_lc:
            robot_match = k
            for hint_k, hint_v in v.items():
                if isinstance(hint_v, list):
                    suggested[hint_k] = list(hint_v)
            break

    # Optional empirical refinement from real sensor data.
    empirical_used = False
    empirical_notes: _List[str] = []
    if real_data_path:
        rp = Path(real_data_path)
        if not rp.exists():
            empirical_notes.append(f"real_data_path not found: {real_data_path}")
        else:
            try:
                rows: _List[Dict[str, str]] = []
                if rp.suffix.lower() == ".json":
                    data = json.loads(rp.read_text())
                    if isinstance(data, list):
                        rows = [r for r in data if isinstance(r, dict)]
                else:
                    with rp.open(newline="") as fh:
                        rows = list(csv.DictReader(fh))
                # Numeric columns -> use min/max as suggested range.
                if rows:
                    keys = rows[0].keys()
                    for key in keys:
                        try:
                            vals = [float(r[key]) for r in rows if r.get(key) not in (None, "")]
                        except (TypeError, ValueError):
                            continue
                        if len(vals) >= 2:
                            lo, hi = min(vals), max(vals)
                            if hi > lo:
                                suggested[f"empirical_{key}"] = [lo, hi]
                                empirical_used = True
                if empirical_used:
                    empirical_notes.append(f"refined ranges from {len(rows)} rows in {rp.name}")
            except Exception as exc:  # robust: never raise to the LLM
                empirical_notes.append(f"failed to parse real_data_path: {exc}")

    return {
        "task_type": task_raw,
        "task_matched": task_key,
        "robot": robot_raw,
        "robot_matched": robot_match,
        "suggested_ranges": suggested,
        "real_data_used": empirical_used,
        "notes": empirical_notes,
        "message": (
            f"Suggested DR ranges for task='{task_raw}' (matched '{task_key}')"
            + (f" robot='{robot_raw}' (matched '{robot_match}')" if robot_match else "")
            + (f"; refined with empirical data" if empirical_used else "")
        ),
    }


