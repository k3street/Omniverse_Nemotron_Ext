class TestRoutesCacheKey:

    def test_same_goal_same_key(self):
        from service.isaac_assist_service.manipulation.routes import _cache_key
        snap = _snapshot("obj1")
        assert _cache_key("pick up the mug", snap) == _cache_key("pick up the mug", snap)

