class TestTaskSpecValidation:

    def test_minimal_valid_spec(self):
        spec = _pick_place_spec()
        assert spec.spec_version == "1.0"
        assert len(spec.phases) == 2

    def test_task_id_autogenerated(self):
        spec = _pick_place_spec()
        assert len(spec.task_id) == 36  # UUID4 string

    def test_contiguous_phase_indices_required(self):
        with pytest.raises(Exception):  # pydantic ValidationError
            snap = _snapshot("obj1")
            TaskSpec(
                goal_text="test",
                scene_snapshot=snap,
                phases=[
                    Phase(
                        phase_index=0,
                        skill_name="pick_rigid",
                        hand_assignment={"right": HandRole.LEAD, "left": HandRole.IDLE},
                        semantic_target=ObjectGraspTarget(type="object_grasp", object_id="obj1"),
                        success_predicate=ObjectAttachedPredicate(
                            type="object_attached", object_id="obj1", arm="right"
                        ),
                        failure_predicate=DurationExceededPredicate(
                            type="duration_exceeded", threshold_s=5.0
                        ),
                    ),
                    Phase(
                        phase_index=2,  # skipped index 1 → invalid
                        skill_name="place_on_surface",
                        hand_assignment={"right": HandRole.LEAD, "left": HandRole.IDLE},
                        semantic_target=ObjectGraspTarget(type="object_grasp", object_id="obj1"),
                        success_predicate=DurationExceededPredicate(
                            type="duration_exceeded", threshold_s=1.0
                        ),
                        failure_predicate=DurationExceededPredicate(
                            type="duration_exceeded", threshold_s=5.0
                        ),
                    ),
                ],
            )

    def test_unknown_object_id_rejected(self):
        with pytest.raises(Exception):
            snap = _snapshot("obj1")   # only obj1 in scene
            TaskSpec(
                goal_text="test",
                scene_snapshot=snap,
                phases=[
                    Phase(
                        phase_index=0,
                        skill_name="pick_rigid",
                        hand_assignment={"right": HandRole.LEAD, "left": HandRole.IDLE},
                        semantic_target=ObjectGraspTarget(
                            type="object_grasp",
                            object_id="ghost_obj",  # not in scene → invalid
                        ),
                        success_predicate=DurationExceededPredicate(
                            type="duration_exceeded", threshold_s=1.0
                        ),
                        failure_predicate=DurationExceededPredicate(
                            type="duration_exceeded", threshold_s=5.0
                        ),
                    )
                ],
            )

    def test_phase_requires_exactly_one_lead(self):
        with pytest.raises(Exception):
            snap = _snapshot("obj1")
            Phase(
                phase_index=0,
                skill_name="pick_rigid",
                hand_assignment={"right": HandRole.LEAD, "left": HandRole.LEAD},  # two leads
                semantic_target=ObjectGraspTarget(type="object_grasp", object_id="obj1"),
                success_predicate=DurationExceededPredicate(
                    type="duration_exceeded", threshold_s=1.0
                ),
                failure_predicate=DurationExceededPredicate(
                    type="duration_exceeded", threshold_s=5.0
                ),
            )

    def test_phase_with_no_lead_rejected(self):
        with pytest.raises(Exception):
            Phase(
                phase_index=0,
                skill_name="pick_rigid",
                hand_assignment={"right": HandRole.IDLE, "left": HandRole.IDLE},  # no lead
                semantic_target=ObjectGraspTarget(type="object_grasp", object_id="x"),
                success_predicate=DurationExceededPredicate(
                    type="duration_exceeded", threshold_s=1.0
                ),
                failure_predicate=DurationExceededPredicate(
                    type="duration_exceeded", threshold_s=5.0
                ),
            )

    def test_empty_phases_rejected(self):
        with pytest.raises(Exception):
            TaskSpec(
                goal_text="test",
                scene_snapshot=_snapshot("obj1"),
                phases=[],
            )

    def test_model_serializes_to_json(self):
        spec = _pick_place_spec()
        raw = spec.model_dump_json()
        parsed = json.loads(raw)
        assert parsed["spec_version"] == "1.0"
        assert len(parsed["phases"]) == 2

    def test_model_roundtrip(self):
        spec = _pick_place_spec()
        restored = TaskSpec.model_validate(spec.model_dump())
        assert restored.goal_text == spec.goal_text
        assert len(restored.phases) == len(spec.phases)

    def test_compound_predicate_and(self):
        pred = CompoundPredicate(
            type="AND",
            clauses=[
                GripperClosedPredicate(type="gripper_closed", arm="right"),
                ObjectAttachedPredicate(type="object_attached", object_id="x", arm="right"),
            ],
        )
        assert pred.type == "AND"
        assert len(pred.clauses) == 2

    def test_nested_compound_predicate(self):
        inner = CompoundPredicate(
            type="OR",
            clauses=[
                DurationExceededPredicate(type="duration_exceeded", threshold_s=5.0),
            ],
        )
        outer = CompoundPredicate(type="AND", clauses=[inner])
        assert outer.type == "AND"


