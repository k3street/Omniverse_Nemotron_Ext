def _pose(x=0.5, y=0.0, z=0.8):
    return Pose(position=[x, y, z], quaternion=[0.0, 0.0, 0.0, 1.0])


def _snapshot(*object_ids):
    return SceneSnapshot(
        timestamp=time.time(),
        objects=[
            SceneObject(
                object_id=oid,
                **{"class": "rigid"},
                pose=_pose(),
                bbox_3d=[0.05, 0.05, 0.05],
                confidence=0.95,
            )
            for oid in object_ids
        ],
    )


def _pick_place_spec(mug_id="mug_01", tray_id="tray_01") -> TaskSpec:
    snap = _snapshot(mug_id, tray_id)
    return TaskSpec(
        goal_text="Pick up the mug and place it on the tray",
        embodiment_id="tenthings_v1_open_arm_bimanual",
        scene_snapshot=snap,
        phases=[
            Phase(
                phase_index=0,
                skill_name="pick_rigid",
                hand_assignment={"right": HandRole.LEAD, "left": HandRole.IDLE},
                semantic_target=ObjectGraspTarget(type="object_grasp", object_id=mug_id),
                success_predicate=ObjectAttachedPredicate(
                    type="object_attached", object_id=mug_id, arm="right"
                ),
                failure_predicate=DurationExceededPredicate(
                    type="duration_exceeded", threshold_s=10.0
                ),
            ),
            Phase(
                phase_index=1,
                skill_name="place_on_surface",
                hand_assignment={"right": HandRole.LEAD, "left": HandRole.IDLE},
                semantic_target=PlaceOnObjectTarget(
                    type="place_on_object",
                    carry_object_id=mug_id,
                    support_object_id=tray_id,
                ),
                success_predicate=CompoundPredicate(
                    type="AND",
                    clauses=[
                        GripperClosedPredicate(type="gripper_closed", arm="right"),
                    ],
                ),
                failure_predicate=DurationExceededPredicate(
                    type="duration_exceeded", threshold_s=12.0
                ),
            ),
        ],
    )


