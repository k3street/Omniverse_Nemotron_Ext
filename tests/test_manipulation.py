class TestSkillRegistry:

    def test_eleven_skills_registered(self):
        assert len(SKILL_REGISTRY) == 11

    def test_all_known_skills_present(self):
        expected = {
            "pick_rigid", "pick_deformable", "place_on_surface",
            "transit_to_pose", "push_object", "press_button", "open_drawer",
            "stabilize_object", "hold_cloth_taut",
            "bimanual_handover", "bimanual_fold",
        }
        assert set(SKILL_REGISTRY.keys()) == expected

    def test_skill_names_sorted(self):
        assert SKILL_NAMES == sorted(SKILL_NAMES)

    def test_is_valid_skill_true(self):
        assert is_valid_skill("pick_rigid")
        assert is_valid_skill("bimanual_fold")

    def test_is_valid_skill_false(self):
        assert not is_valid_skill("unknown_skill")
        assert not is_valid_skill("")

    def test_get_skill_known(self):
        spec = get_skill("pick_rigid")
        assert spec.name == "pick_rigid"
        assert spec.arm_mode == "single"

    def test_get_skill_unknown_raises(self):
        with pytest.raises(KeyError):
            get_skill("nonexistent_skill")

    def test_assist_skills_have_assist_role(self):
        for name in ("stabilize_object", "hold_cloth_taut"):
            spec = get_skill(name)
            assert spec.assist_role is True

    def test_bimanual_skills_are_bimanual_sync(self):
        for name in ("bimanual_handover", "bimanual_fold"):
            spec = get_skill(name)
            assert spec.arm_mode == "bimanual_sync"

    def test_skills_for_prompt_contains_all_names(self):
        text = skills_for_prompt()
        for name in SKILL_NAMES:
            assert name in text

    def test_skills_for_prompt_is_multiline(self):
        assert "\n" in skills_for_prompt()

    def test_all_skills_have_description(self):
        for name, spec in SKILL_REGISTRY.items():
            assert spec.description, f"'{name}' has empty description"

    def test_single_arm_skills_have_no_assist_role(self):
        for name in ("pick_rigid", "pick_deformable", "place_on_surface",
                     "transit_to_pose", "push_object", "press_button", "open_drawer"):
            assert not get_skill(name).assist_role


